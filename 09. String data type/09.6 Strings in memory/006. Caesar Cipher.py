n = int(input())
s = input()
alphabet = 'abcdefghijklmnopqrstuvwxyz'

for c in s:
    # ищем индекс текущей буквы в строке алфавита
    ind = alphabet.index(c)
    # находим новую букву
    new_c = alphabet[ind - n]

    print(new_c, end='')
