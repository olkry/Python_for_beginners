# Two old women walk towards each other with constant speeds V1
# and V2 km/h. Determine after what time the old women will meet,
# if the distance between them is S km.
#
# Две старушки идут навстречу друг другу с постоянными скоростями V1
# и V2 км/ч. Определите, через какое время старушки встретятся,
# если расстояние между ними равно S км.

distance, speed1, speed2 = float(input()), float(input()), float(input())
print(distance / (speed1 + speed2))
